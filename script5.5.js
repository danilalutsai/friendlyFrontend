// Оперант НЕ то есть NOT преобразует значение в противоположное. 
// Если значение было истинным, то результатом будет ложь, и наоборот.

console.log(!true) // false
console.log(!false) // true

console.log(!'') // false
console.log(!'Привет') // false
console.log(!0) // false
console.log(!111) // false
