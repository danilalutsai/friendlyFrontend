// Преобразование Чисел в Булевы Значения

console.log(Boolean(-1))
console.log(Boolean(0))
console.log(Boolean(1))
console.log(Boolean(2))

// Преобразование Строкв Булевы Значения

console.log(Boolean('Привет!'))
console.log(Boolean('0'))
console.log(Boolean(' '))
console.log(Boolean(''))

// Булевые значения которые преобразуются в False

console.log(Boolean(0))
console.log(Boolean(''))
console.log(Boolean(null))
console.log(Boolean(undefined))
console.log(Boolean(NaN))

// Все остальные значения преобразуются в True

