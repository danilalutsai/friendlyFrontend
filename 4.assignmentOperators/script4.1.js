// Бинарные операторы
console.log(2 + 2)
console.log(10 - 1)
console.log(5 * 10)
console.log(100 / 25)

// Возведение в степень
console.log(2 ** 10) // 2 в 10 степени

// Унарные операторы пишутся вместе с числом
console.log(-5) // -5
console.log(+5) // 5

console.log(+true) // 1
console.log(+false) // 0
console.log(+"") // 0
console.log(+" ") // 0
console.log(+"abc") // NaN
console.log(+null) // 0 
console.log(+undefined) // NaN

console.log('3' + '5') // '35' - конкатенация строк
console.log(+'3' + +'5') // 8 - сначала строки преобразуются в числа, а потом складываются

